package org.Day_6th_LocalGlobalVariables;

public class GlobalVeriableDefaultValue {

		int a=10;//global variables Default value
		int b=20;
		int sum=a+b;
		int subtraction=a-b;
		int multiplication=a*b;
		int division=a/b;
		public static void main(String[] args) {
			GlobalVeriableDefaultValue obj = new GlobalVeriableDefaultValue();
		System.out.println("sum ="+obj.sum);
		System.out.println("subtraction ="+obj.subtraction);
		System.out.println("subtraction ="+obj.multiplication);
		System.out.println("subtraction ="+obj.division);
		
	}

}
