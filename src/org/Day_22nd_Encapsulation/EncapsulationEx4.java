package org.Day_22nd_Encapsulation;

public class EncapsulationEx4 {
	private float a,b,c;
	public float getA() {
		return a;
	}
	public void setA(float a) {
		this.a = a;
	}
	public float getB() {
		return b;
	}
	public void setB(float b) {
		this.b = b;
	}
	public float getC() {
		return c;
	}
	public void setC(float c) {
		this.c = c;
	}
	public boolean getX() {
		return x;
	}
	public void setX(boolean x) {
		this.x = x;
	}
	public boolean getY() {
		return y;
	}
	public void setY(boolean y) {
		this.y = y;
	}
	public boolean getZ() {
		return z;
	}
	public void setZ(boolean z) {
		this.z = z;
	}
	public byte getJ() {
		return j;
	}
	public void setJ(byte j) {
		this.j = j;
	}
	public byte getK() {
		return k;
	}
	public void setK(byte k) {
		this.k = k;
	}
	public byte getL() {
		return l;
	}
	public void setL(byte l) {
		this.l = l;
	}
	private boolean x,y,z;
	private byte j,k,l;
}
