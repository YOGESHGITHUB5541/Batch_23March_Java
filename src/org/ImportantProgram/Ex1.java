package org.ImportantProgram;

public class Ex1 {

	public static void main(String[] args) {
		int [] ar = {0,1,0,1,0,1,0,1};
		for(int i=0; i<ar.length; i++) {
			if(ar[i]==0)
			System.out.print(ar[i]);
				
			}
		for(int j=0; j<ar.length; j++) {
			if(ar[j]!=0)
			System.out.print(ar[j]);
		}
	}

}
