package org.Day_13th_WhileDoWhile;

public class WhileLoopEx1 {
	public static void main(String[]args) {
		char i='A';               //variable initialization
		while(i<='Z') {           //condition
			System.out.println("while loop ="+i); //body
			i++;                 //increment or Decrement
		}
		
	}

}
