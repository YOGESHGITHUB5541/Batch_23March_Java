package org.Day_13th_WhileDoWhile;

public class DoWhileEx1 {
	public static void main(String[]args) {
		int i=1;
		do {
			System.out.println(i);
			i++;     //increment
		}while(i>=50); //condition true
			
		
	}

}
//in do while loop when condition is false then execute at least ones 
//above Example when condition false then print only 50 